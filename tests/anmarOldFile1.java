class CalculatorOld {

    public int add(int a, int b) {
        int result = a + b;
        return result;
    }

    public int subtract(int a, int b) {
        return a - b;
    }

    public int multiply(int a, int b) {
        int product = a * b;
        return product;
    }

    public double power(double base, double exponent) {
        return Math.pow(base, exponent);
    }

}
